#!/bin/bash
# 子智能体任务分派标准模板
# 用途：确保每次分派任务时都包含明确要求和验证标准

echo "=========================================="
echo "📋 子智能体任务分派标准模板"
echo "=========================================="
echo ""

cat << 'TEMPLATE'
## 标准分派话术（必须包含以下要素）

```javascript
sessions_spawn({
  agentId: "{agentId}",
  label: "{固定 label}",
  mode: "run",
  task: `你是{agent 名称}，负责{任务类型}。

## 核心要求（必须遵守）

1. **发现错误必须分析原因**，禁止只汇报"失败了"
2. **必须主动尝试修复**（换日期、改参数、重试等）
3. **必须验证修复结果**
4. **汇报格式（强制）**：
   - 问题现象
   - 原因分析
   - 修复尝试
   - 验证结果
   - 结论建议

## 示例

看到错误时：
- ❌ 禁止：汇报"接口失败"
- ✅ 必须：分析原因 → 尝试修复 → 验证结果 → 汇报

## 验收标准

- 每个任务必须有明确的结果（成功/失败）
- 失败的任务必须有原因分析和修复尝试
- 汇报必须包含完整 5 要素
- **缺少任何要素 → 打回重新汇报**

开始执行。`
})
```

## 验证机制

### 中途检查（5 分钟后）
- 检查是否开始执行
- 检查是否遇到错误
- 检查是否在主动修复

### 汇报验证
- 检查 5 要素是否完整
- 缺少要素 → 打回重新汇报
- 格式不符 → 拒绝接收

### 违规处理
- 第 1 次：指出错误，要求纠正
- 第 2 次：批评并记录
- 第 3 次：影响下次任务分派

